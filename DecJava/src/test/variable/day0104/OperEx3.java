package test.variable.day0104;

public class OperEx3 {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		boolean c = a < b;
		System.out.println(c);
		
		c = a == b;
		System.out.println(c);
		
		c = a != b;
		System.out.println(c);

		String s1 = "ABC";
		String s2 = "ABC";
		System.out.println(s1 == s2);
		
	}

}









